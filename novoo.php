Cluster é um termo em inglês que significa “aglomerar” ou “aglomeração” e pode ser aplicado em vários contextos.
No caso da computação, o termo define uma arquitetura de sistema capaz combinar vários computadores para trabalharem em conjunto 
ou pode denominar o grupo em si de computadores combinados.

De forma resumida, o Docker é uma plataforma de código aberto, desenvolvido na linguagem Go e criada pelo próprio Docker. Inc.
Por ser de alto desempenho, o software garante maior facilidade na criação e administração de ambientes isolados,
garantindo a rápida disponibilização de programas para o usuário final

